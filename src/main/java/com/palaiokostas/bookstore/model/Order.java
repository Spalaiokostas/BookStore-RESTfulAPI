package com.palaiokostas.bookstore.model;

import com.palaiokostas.bookstore.model.dataTypes.OrderStatus;
import java.util.Date;
import javax.persistence.Entity;
import javax.persistence.EnumType;
import javax.persistence.Enumerated;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import javax.validation.constraints.NotNull;
import org.springframework.data.annotation.CreatedDate;

/**
 *
 * @author Spyros Palaiokostas
 */

@Entity
@Table(name = "orders")
public class Order {
    
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;
    
    @ManyToOne
    @JoinColumn(name = "user_id")
    private User user;
    
    @ManyToOne
    @JoinColumn(name = "book_id")
    private Book book;
    
    @NotNull
    private int quantity;
    
    @Temporal(TemporalType.TIMESTAMP)
    @CreatedDate
    private Date submission_date;
    
    @Enumerated(EnumType.STRING)
    private OrderStatus status;
    
    public Order() {
        
    }
    
    /* used by testing */
    public Order(int id, User user, Book book, int quantity, 
                Date sub_date, OrderStatus status) 
    {
        this.id = id;
        this.user = user;
        this.book = book;
        this.quantity = quantity;
        this.submission_date = sub_date;
        this.status = status;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the user
     */
    public User getUser() {
        return user;
    }

    /**
     * @param user the user to set
     */
    public void setUser(User user) {
        this.user = user;
    }

    /**
     * @return the book
     */
    public Book getBook() {
        return book;
    }

    /**
     * @param book the book to set
     */
    public void setBook(Book book) {
        this.book = book;
    }

    /**
     * @return the quantity
     */
    public int getQuantity() {
        return quantity;
    }

    /**
     * @param quantity the quantity to set
     */
    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    /**
     * @return the submission_date
     */
    public Date getSubmission_date() {
        return submission_date;
    }

    /**
     * @param submission_date the submission_date to set
     */
    public void setSubmission_date(Date submission_date) {
        this.submission_date = submission_date;
    }

    /**
     * @return the status
     */
    public OrderStatus getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(OrderStatus status) {
        this.status = status;
    }
    
    
    
}
